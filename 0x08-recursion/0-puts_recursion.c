#include "holberton.h"

/**
 * _puts_recursion - check the code for Holberton School students.
 * @s: The character to print
 * The character to print
 * Return: Always 0.
 */

void _puts_recursion(char *s)
{
	int i;

	if (*s)
	{
		for (i = 0; i[s] != '\0'; i++)
		_putchar(i[s]);
		_putchar('\n');
	}
}
